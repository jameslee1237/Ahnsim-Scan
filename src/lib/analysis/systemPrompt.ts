export const buildUserContent = (input: AnalysisInput): string => {
  if (input.type === 'sms') {
    return [
      '다음 문자 메시지를 분석하세요.',
      `발신번호: ${input.senderNumber}`,
      '<message_to_analyze>',
      input.messageBody,
      '</message_to_analyze>',
    ].join('\n');
  }

  return [
    '다음 이메일을 분석하세요.',
    `발신 주소: ${input.senderAddress}`,
    `제목: ${input.subject}`,
    '<message_to_analyze>',
    input.body,
    '</message_to_analyze>',
  ].join('\n');
};
